#include <stdio.h>
int main(void)
{
	int num;
	// xuat ra man hinh 1 den 10 tren 1 hang
	for(num = 1; num <= 10; num = num + 1){
		printf("%d\t  ", num);
		}
	// xuat ra man hinh 1 den 10 tren 10 dong
	for(num = 1; num <= 10; num = num + 1){
		printf("%d \n", num);
		}
		return 0;
}

